#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - function that returns sum of a and b
 * @a: first number will be operated
 * @b: second number will be operated
 * Return: (a + b)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that returns diff of a and b
 * @a: first number will be operated
 * @b: second number will be operated
 * Return: (a - b)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - f that return product of a and b
 * @a: first number will be operated
 * @b: second number will be operated
 * Return: (a * b)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that return division of a by b
 * @a: first number
 * @b: second number
 * Return: (a / b)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder of the division of a by b
 * @a: first number input
 * @b: second number input
 * Return: (a % b)
 */

int op_mod(int a, int b)
{
	return (a % b);
}













